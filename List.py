
# List functions

#  Generate List
print('----------------------------------------------------')
thislist = ["apple", "banana", "cherry"]
print(thislist)




#  append element in existing list
print('----------------------------------------------------')
thislist = ["apple", "banana", "cherry"]
thislist.append("orange")
print(thislist)




# This will pop the last element of lIST
print('----------------------------------------------------')
thislist = ["apple", "banana", "cherry"]
thislist.pop()
print(thislist)




# This will remove specific item
print('----------------------------------------------------')
thislist = ["apple", "banana", "cherry"]
thislist.remove("banana")
print(thislist)




# insert element at specific position
print('----------------------------------------------------')
thislist = ["apple", "banana", "cherry"]
thislist.insert(1, "orange")
print(thislist)




# this will clear the list
print('----------------------------------------------------')
thislist = ["apple", "banana", "cherry"]
thislist.clear()
print(thislist)





# print length of list
print('----------------------------------------------------')
thislist = ["apple", "banana", "cherry"]
print(len(thislist))




#  print second index item
print('----------------------------------------------------')
thislist = ["apple", "banana", "cherry"]
print(thislist[1])




#  change value of an item
print('----------------------------------------------------')
thislist = ["apple", "banana", "cherry"]
thislist[1] = "blackcurrant"
print(thislist)




#  remove item from specific position
print('----------------------------------------------------')
thislist = ["apple", "banana", "cherry"]
del thislist[0]
print(thislist)
